package org.scauhci.ExamSystem.android.pojo;

public class RelationPaperQuestionPojo {

	private String relationPaperQuestionId;
	private String examId;
	private String paperId;
	private String questionId;

	public String getRelationPaperQuestionId() {
		return relationPaperQuestionId;
	}

	public void setRelationPaperQuestionId(String relationPaperQuestionId) {
		this.relationPaperQuestionId = relationPaperQuestionId;
	}

	public String getExamId() {
		return examId;
	}

	public void setExamId(String examId) {
		this.examId = examId;
	}

	public String getPaperId() {
		return paperId;
	}

	public void setPaperId(String paperId) {
		this.paperId = paperId;
	}

	public String getQuestionId() {
		return questionId;
	}

	public void setQuestionId(String questionId) {
		this.questionId = questionId;
	}

}
